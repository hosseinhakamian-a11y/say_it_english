<p><strong>فرمول ۳ مرحله‌ای پاسخگویی (Part 3):</strong></p>
<ol>
  <li><strong>Answer:</strong> جواب مستقیم بدهید.</li>
  <li><strong>Explain:</strong> دلیل بیاورید (Why?).</li>
  <li><strong>Example:</strong> مثال شخصی یا عمومی بزنید.</li>
</ol>

