<h2>متد جعبه لایتنر (Leitner Box) چیست؟</h2>
<p>این سیستم بر اساس <strong>تکرار فاصله‌دار (Spaced Repetition)</strong> کار می‌کند. به جای اینکه یک لغت را ۱۰۰ بار در یک روز بخوانید، آن را ۵ بار در طول یک ماه می‌خوانید.</p>
<ul>
  <li>روز اول: یادگیری</li>
  <li>روز دوم: مرور اول</li>
  <li>روز چهارم: مرور دوم</li>
  <li>روز هشتم: مرور سوم</li>
  <li>روز پانزدهم: مرور نهایی</li>
</ul>

