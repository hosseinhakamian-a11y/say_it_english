<p>وقتی در خانه تنها هستید، کارهایتان را گزارش دهید. </p>
<p><em>"I am opening the fridge. Looking for some eggs. Oh, we ran out of milk."</em></p>
<p>این کار شاید احمقانه به نظر برسد، اما "دیوارِ ترجمه ذهنی" را فرو می‌ریزد و باعث می‌شود انگلیسی فکر کنید.</p>

