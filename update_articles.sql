<h3>۱. لیسنینگ (Listening): طلایِ آسان</h3>
<p>این تنها بخشی است که می‌توانید در آن نمره ۹ بگیرید و معدل کل را بالا بکشید. راز آن <strong>Active Listening</strong> است.</p>
<ul>
  <li><strong>تمرین:</strong> پادکست‌های BBC 6 Minute English را گوش دهید.</li>
  <li><strong>تکنیک سایه:</strong> هر جمله را می‌شنوید، پاز کنید و عیناً تکرار کنید.</li>
  <li><strong>دام‌ها:</strong> مراقب Distractorها باشید (گوینده جوابی را می‌گوید و بعد اصلاحش می‌کند: "It was at 5 PM... oh wait, sorry, 5:30").</li>
</ul>

<h3>۲. ریدینگ (Reading): مدیریت بحران زمان</h3>
<p>شما ۶۰ دقیقه وقت دارید و ۳ متن طولانی. خواندن کلمه به کلمه یعنی خودکشی!</p>
<ul>
  <li><strong>Skimming:</strong> فقط عنوان، زیرتیترها و جملات اول هر پاراگراف را بخوانید تا "نقشه متن" بیاید دستتان (۲ دقیقه).</li>
  <li><strong>Scanning:</strong> دنبال کلمات کلیدی (اعداد، اسامی خاص) بگردید تا جای جواب را پیدا کنید.</li>
</ul>

<h3>۳. رایتینگ (Writing): ریاضیات کلمات</h3>
<p>رایتینگ آیلتس هنر نیست، علم است. اگزمینر دنبال لغات شکسپیری نیست؛ دنبال Collocationهای دقیق است.</p>
<p>به جای گفتن <em>"Prices went up"</em> بگویید <em>"Prices experienced a significant surge"</em>. همین تغییر ساده، نمره شما را از ۵ به ۷ می‌برد.</p>

<h3>۴. اسپیکینگ (Speaking): نمایش اعتماد به نفس</h3>
